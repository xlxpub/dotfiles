🎯 快速开始（3 步）：

  第 1 步：打开 Neovim 进行首次初始化
  $ nvim

  第 2 步：安装 LSP 服务器
  在 Neovim 中输入：
  :Mason

  然后：
  - 搜索 "gopls" → 按 'i' 安装
  - 搜索 "lua_ls" → 按 'i' 安装
  - 按 'q' 退出

  第 3 步：开始使用！
  $ v <filename>   # 或使用别名 vi, vim


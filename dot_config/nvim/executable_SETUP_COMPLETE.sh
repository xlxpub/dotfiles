  文件操作：
  • <Space>w    - 保存文件
  • <Space>q    - 退出
  • <Space>e    - 打开/关闭文件树

  搜索导航：
  • <Space>ff   - 查找文件
  • <Space>fg   - 全文搜索
  • <Space>fb   - 搜索 Buffer
  • <Space>a    - 打开符号大纲

  代码编辑：
  • gd          - 跳转到定义
  • gr          - 查看引用
  • K           - 查看文档
  • <Space>rn   - 重命名
  • <Space>ca   - Code Action

  Git：
  • ]h / [h     - 下一个/上一个变更
  • <Space>hp   - 预览变更
  • <Space>hb   - 查看 blame

